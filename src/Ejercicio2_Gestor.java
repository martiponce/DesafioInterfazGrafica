    private void agregarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarButtonActionPerformed
        String nombreTarea = nuevaTareaField.getText().trim();

    if (!nombreTarea.isEmpty()) {
        Tarea nuevaTarea = new Tarea(nombreTarea);
        modeloLista.addElement(nuevaTarea);
        nuevaTareaField.setText("");
    } else {
        JOptionPane.showMessageDialog(this,
            "Por favor, ingrese un nombre para la tarea.",
            "Error",
            JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_agregarButtonActionPerformed

    private void completarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_completarButtonActionPerformed
        Tarea tareaSeleccionada = tareasList.getSelectedValue();

    if (tareaSeleccionada != null) {
        tareaSeleccionada.setCompletada(true);
        tareasList.repaint(); // Obliga a la lista a redibujarse
    } else {
        JOptionPane.showMessageDialog(this,
            "Por favor, seleccione una tarea de la lista.",
            "Aviso",
            JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_completarButtonActionPerformed

